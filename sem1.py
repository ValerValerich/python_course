import calendar


def disc():
    a, b, c = 5, -10, -400
    discriminant = b ** 2 - 4 * a * c
    if discriminant > 0:
        x1 = (-b + discriminant ** 0.5) / (2 * a)
        x2 = (-b - discriminant ** 0.5) / (2 * a)
        print(x1, x2)
    elif discriminant == 0:
        x = -b / (2 * a)
        print(x)
    else:
        print("–ö–æ—Ä–Ω–µ–π –Ω–µ—Ç!")


