def decorator(loop: int):
    def inner(func):
        res = []

        def wrapper(*args, **kwargs):
            for _ in range(loop):
                res.append(func(*args, **kwargs))
            return res

        return wrapper

    return inner


@decorator(5)
def func_under_decorator(a, b):
    return a + b


print(func_under_decorator(1, 9))



